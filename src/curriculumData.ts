// Helper to convert numbers to Eastern Arabic format (١٢٣...)
export function toEasternArabicNumerals(num: string | number): string {
  const arabicDigits = ['٠', '١', '٢', '٣', '٤', '٥', '٦', '٧', '٨', '٩'];
  return num
    .toString()
    .replace(/[0-9]/g, (w) => arabicDigits[+w]);
}
